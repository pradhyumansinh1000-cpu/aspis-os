# ─── Lifecycle Events ─────────────────────────────────────────────────────────
@app.on_event("startup")
async def startup_event():
    """
    Called once when the server starts.
    - Verifies DB connection
    - In development: creates tables (production uses Alembic)
    - Logs startup info
    """
    logger.info(
        "Starting StudentAI Platform",
        env=settings.APP_ENV,
        debug=settings.DEBUG,
        llm_model=settings.LLAMA_MODEL,
    )

    if settings.APP_ENV == "development":
        # Only auto-create tables in development
        # Production: always use `alembic upgrade head`
        try:
            await init_db()
            logger.info("Database tables initialized (development mode)")
        except Exception as e:
            logger.warning("DB init skipped", error=str(e))


@app.on_event("shutdown")
async def shutdown_event():
    """Gracefully close DB and Redis connections on server shutdown."""
    logger.info("Shutting down StudentAI Platform")
    await close_db()
    await close_redis()


