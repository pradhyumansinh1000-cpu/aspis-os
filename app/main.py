# ─── Health Check ─────────────────────────────────────────────────────────────
@app.get("/health", tags=["System"], summary="Health check for load balancer/Docker")
async def health_check():
    """
    Used by Docker, Kubernetes, and load balancers to verify the app is running.
    Returns DB and Redis connectivity status.
    """
    from app.database import engine
    import redis.asyncio as aioredis
    from app.redis_client import get_redis_pool

    db_ok = False
    redis_ok = False

    try:
        async with engine.connect() as conn:
            await conn.execute(__import__("sqlalchemy", fromlist=["text"]).text("SELECT 1"))
        db_ok = True
    except Exception:
        pass

    try:
        r = aioredis.Redis(connection_pool=get_redis_pool())
        await r.ping()
        redis_ok = True
    except Exception:
        pass

    status_code = 200 if (db_ok and redis_ok) else 503
    return JSONResponse(
        status_code=status_code,
        content={
            "status": "healthy" if (db_ok and redis_ok) else "degraded",
            "services": {
                "database": "ok" if db_ok else "error",
                "redis": "ok" if redis_ok else "error",
                "api": "ok",
            },
            "version": "1.0.0",
            "environment": settings.APP_ENV,
        },
    )


